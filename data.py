def load_questions():
    rows = []
    module, position, _ = MODULES["M"]
    for dim in FUNCTIONS:
        for index, text in enumerate(CORE_POSITIVE[dim], start=1):
            rows.append({
                "qid": f"M_{dim}_P{index:02d}",
                "module": module,
                "position": position,
                "dimension": dim,
                "principle": PRINCIPLES[dim],
                "front_text": text,
                "scoring_key": f"{dim}_CorePositive",
            })
        for index, text in enumerate(CORE_AVERSION[dim], start=1):
            rows.append({
                "qid": f"M_{dim}_A{index:02d}",
                "module": module,
                "position": position,
                "dimension": dim,
                "principle": PRINCIPLES[dim],
                "front_text": text,
                "scoring_key": f"{dim}_CoreAversion",
            })

    for prefix in ["Z", "G", "E", "X"]:
        module, position, scoring_suffix = MODULES[prefix]
        for dim in FUNCTIONS:
            for index, text in enumerate(QUESTION_TEXTS[prefix][dim], start=1):
                rows.append({
                    "qid": f"{prefix}_{dim}_{index:02d}",
                    "module": module,
                    "position": position,
                    "dimension": dim,
                    "principle": PRINCIPLES[dim],
                    "front_text": text,
                    "scoring_key": f"{dim}_{scoring_suffix}",
                })
    return rows


